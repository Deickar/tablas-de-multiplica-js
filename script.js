// tabla de multiplica del 1
let nuemero1 = 1;
console.log("Tabla del 1");
// utilzamos el bucle for para hacer la tabla
for (let i = 1; i <= 10; i++) {
  console.log(nuemero1, "x", i, "=", nuemero1 * i);
}
// salto de linea
console.log("\n");

